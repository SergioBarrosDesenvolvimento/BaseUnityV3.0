Citizen.CreateThread(function()
    for _, v in pairs(Config.Zones) do
        local blip = AddBlipForRadius(v[1], v[2], v[3], v[4] + 0.0)
        SetBlipAlpha(blip, 100)
        SetBlipColour(blip, v[5])
    end
end)
