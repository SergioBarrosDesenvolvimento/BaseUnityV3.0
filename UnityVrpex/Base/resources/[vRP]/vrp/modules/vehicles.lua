local veh_list = module("cfg/vehicles")
local vehglobal = veh_list.vehicles
-----------------------------------------------------------------------------------------------------------------------------------------
-----------------------------------------------------------------------------------------------------------------------------------------
-- [EXPORTANDO PARA O SERVER DO VRP]
-----------------------------------------------------------------------------------------------------------------------------------------
-----------------------------------------------------------------------------------------------------------------------------------------
-- VEHICLEGLOBAL
-----------------------------------------------------------------------------------------------------------------------------------------
function vRP.vehicleGlobal()
	return vehglobal
end
-----------------------------------------------------------------------------------------------------------------------------------------
-- VEHICLENAMEBONITO
-----------------------------------------------------------------------------------------------------------------------------------------
function vRP.vehicleNameSpawn(car)
	if vehglobal[car] ~= nil then
		return vehglobal[car]
	end
end
-----------------------------------------------------------------------------------------------------------------------------------------
-- VEHICLENAME
-----------------------------------------------------------------------------------------------------------------------------------------
function vRP.vehicleName(vname)
	return vehglobal[vname].name
end
-----------------------------------------------------------------------------------------------------------------------------------------
-- VEHICLEPRICE
-----------------------------------------------------------------------------------------------------------------------------------------
function vRP.vehiclePrice(vname)
	return vehglobal[vname].price
end
-----------------------------------------------------------------------------------------------------------------------------------------
-- VEHICLEPRICE2
-----------------------------------------------------------------------------------------------------------------------------------------
function vRP.vehiclePrice2(vname)
	return vehglobal[vname].price2
end
-----------------------------------------------------------------------------------------------------------------------------------------
-- VEHICLETYPE
-----------------------------------------------------------------------------------------------------------------------------------------
function vRP.vehicleType(vname)
	return vehglobal[vname].tipo
end
-----------------------------------------------------------------------------------------------------------------------------------------
-- VEHICLETYPE
-----------------------------------------------------------------------------------------------------------------------------------------
function vRP.vehicleModel(vname)
	return vehglobal[vname].modelo
end
-----------------------------------------------------------------------------------------------------------------------------------------
-- VEHICLECHEST
-----------------------------------------------------------------------------------------------------------------------------------------
function vRP.vehicleMala(vname)
	return vehglobal[vname].mala
end
-----------------------------------------------------------------------------------------------------------------------------------------
-- VEHICLEBANNED
-----------------------------------------------------------------------------------------------------------------------------------------
function vRP.vehicleBanned(vname)
	return vehglobal[vname].banned
end